
package modulo_4;

public class ClasseMath {

    public static void main(String[] args) {
        
        System.out.println("Eleva 5 ao cubo: " + Math.pow(5,4));
        System.out.println("Raís quadrada de 25: " + Math.sqrt(25));
        System.out.println("Logaritimo de 128: " + Math.log(128));
        System.out.println("Valor absoluto: " + Math.abs(-3));
        System.out.println("Arredonda para cima: " + Math.ceil(3.5));
        System.out.println("Arredonda para baixo: " + Math.floor(3.5));
        System.out.println("Arredonda: " + Math.round(4.5));
        System.out.println("Valor randônico entre 0 e 1: " + Math.random());
        System.out.println("Maior valor: " + Math.max(10, 2));
        System.out.println("Menor valor: " + Math.min(10, 2));
        
    }
    
}
