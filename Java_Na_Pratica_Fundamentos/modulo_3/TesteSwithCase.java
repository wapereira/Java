
package modulo_3;

//Switch Case = teste de caso, caso o teste for verdadeiro,
//interrompe o código e retorna o valor do caso testado.

//default: Caso nenhuma condição seja verdadeira retorna o valor padrão.

public class TesteSwithCase {

    public static void main(String[] args) {
        int num =0;
        
        switch (num){
            case 10:
                System.out.println("O valor é 10");
                break;
            case 5:
                System.out.println("O valor é 5");
                break;
            case 0:
                System.out.println("O valor é 0");
                break;
        }
    }
    
}
